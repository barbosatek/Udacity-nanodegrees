// Encapsulates sprites.
class Sprite {
    constructor(spritePath, width, height, widthPadding, heightPadding) {
      this.path = spritePath;
      this.width = width;
      this.height = height;
      this.widthPadding = heightPadding;
      this.heightPadding = heightPadding;
    }
  }
  